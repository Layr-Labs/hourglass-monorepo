// CompleteOperatorSetup performs full operator setup through AVS registration
func (h *TestHarness) CompleteOperatorSetup() error {
	// 1. Create keystores
	h.CreateTestKeystores(h.t)

	// 2. Register as operator
	if err := h.RegisterTestOperator(); err != nil {
		return err
	}

	// 3. Register keys
	if err := h.RegisterTestOperatorKeys(); err != nil {
		return err
	}

	// 4. Register with AVS
	result, err := h.ExecuteCLI("register-avs",
		"--context", h.ContextName,
		"--keystore", "operator-ecdsa",
		"--avs", h.ChainConfig.AVSTaskRegistrarAddress,
		"--operator-set-ids", "0",
		"--socket", "http://operator.example.com:8080")
	if err != nil {
		return err
	}
	if result.ExitCode != 0 {
		return fmt.Errorf("AVS registration failed: %s", result.Stderr)
	}

	return nil
}

