// NewTestHarness creates a new test harness instance
func NewTestHarness(t *testing.T) *TestHarness {
	// findProjectRoot returns the directory with go.mod (hgctl-go)
	hgctlRoot := findProjectRoot(t)

	// Create temporary test directory
	testDir, err := os.MkdirTemp("", "hgctl-integration-test-*")
	require.NoError(t, err)

	// Create test logger
	logger := zaptest.NewLogger(t)

	h := &TestHarness{
		TestDir:     testDir,
		ContextName: fmt.Sprintf("test-%d", time.Now().Unix()),
		cliPath:     filepath.Join(hgctlRoot, "bin", "hgctl"),
		logger:      logger,
		t:           t,
		cleanupFns:  []func(){},
	}

	// Add test directory cleanup
	h.cleanupFns = append(h.cleanupFns, func() {
		os.RemoveAll(testDir)
	})

	return h
}

// Setup initializes the test environment
func (h *TestHarness) Setup() error {
	h.logger.Info("Setting up test harness")

	// 1. Initialize chain manager
	h.chainManager = NewChainManager(h.logger)

	// 2. Ensure chains are running
	if err := h.chainManager.EnsureChainsRunning(); err != nil {
		return fmt.Errorf("failed to ensure chains are running: %w", err)
	}

	// Add chain cleanup
	h.cleanupFns = append(h.cleanupFns, func() {
		h.chainManager.Cleanup()
	})

	// 3. Load chain configuration
	config, err := h.chainManager.LoadChainConfig()
	if err != nil {
		return fmt.Errorf("failed to load chain config: %w", err)
	}
	h.ChainConfig = config

	// Set runtime values
	h.ChainConfig.L1ChainID = 31337
	h.ChainConfig.L2ChainID = 31338
	h.ChainConfig.L1RPC = "http://localhost:8545"
	h.ChainConfig.L2RPC = "http://localhost:9545"

	// 3. Wait for chains to be ready
	h.logger.Info("Waiting for chains to be ready")
	l1Client, err := ethclient.Dial(h.ChainConfig.L1RPC)
	if err != nil {
		return fmt.Errorf("failed to connect to L1: %w", err)
	}
	h.L1Client = l1Client

	l2Client, err := ethclient.Dial(h.ChainConfig.L2RPC)
	if err != nil {
		return fmt.Errorf("failed to connect to L2: %w", err)
	}
	h.L2Client = l2Client

	// Wait for both chains
	if err := h.chainManager.WaitForAnvil(l1Client); err != nil {
		return fmt.Errorf("L1 not ready: %w", err)
	}
	if err := h.chainManager.WaitForAnvil(l2Client); err != nil {
		return fmt.Errorf("L2 not ready: %w", err)
	}

	// 4. Initialize contract client
	// Create a proper logger that implements the logger.Logger interface
	testLogger := logger.NewLogger(true) // verbose mode for tests
	contractClient, err := client.NewContractClientWithSigner(
		h.ChainConfig.L1RPC,
		h.ChainConfig.AVSAccountPrivateKey,
		testLogger,
	)
	if err != nil {
		return fmt.Errorf("failed to create contract client: %w", err)
	}
	h.contracts = contractClient

	// 5. Create test context by writing config file
	// Since hgctl doesn't have a 'context create' command, we'll create
	// a minimal config file with our test context
	if err := h.createTestContext(); err != nil {
		return fmt.Errorf("failed to create test context: %w", err)
	}

	h.logger.Info("Test harness setup complete")
	return nil
}

// Teardown cleans up all test resources
func (h *TestHarness) Teardown() {
	h.logger.Info("Tearing down test harness")

	// Execute cleanup functions in reverse order
	for i := len(h.cleanupFns) - 1; i >= 0; i-- {
		h.cleanupFns[i]()
	}

	// Close clients
	if h.L1Client != nil {
		h.L1Client.Close()
	}
	if h.L2Client != nil {
		h.L2Client.Close()
	}
}

// ExecuteCLI runs hgctl with the given arguments
func (h *TestHarness) ExecuteCLI(args ...string) (*CLIResult, error) {
	executor := NewCLIExecutor(h.cliPath, h.TestDir)

	// Set environment variables for the test context
	// Note: The executor already sets HGCTL_CONFIG_DIR to use our test config
	if h.ChainConfig != nil {
		// Add 0x prefix to private key if not present
		privateKey := h.ChainConfig.OperatorAccountPrivateKey
		if !strings.HasPrefix(privateKey, "0x") {
			privateKey = "0x" + privateKey
		}
		executor.Env = append(executor.Env,
			fmt.Sprintf("ETH_RPC_URL=%s", h.ChainConfig.L1RPC),
			fmt.Sprintf("PRIVATE_KEY=%s", privateKey),
		)
	}

	return executor.Execute(args...)
}

