// createTestContext creates a minimal context config file for testing
func (h *TestHarness) createTestContext() error {
	// Create config directory
	configDir := filepath.Join(h.TestDir, ".hgctl")
	if err := os.MkdirAll(configDir, 0755); err != nil {
		return fmt.Errorf("failed to create config directory: %w", err)
	}

	// Create minimal config with test context
	contextConfig := map[string]interface{}{
		"rpcUrl":          h.ChainConfig.L1RPC,
		"networkId":       h.ChainConfig.L1ChainID,
		"executorAddress": "127.0.0.1:9090", // Default executor address
	}

	config := map[string]interface{}{
		"currentContext": h.ContextName,
		"contexts": map[string]interface{}{
			h.ContextName: contextConfig,
		},
	}

	// Write config file
	configFile := filepath.Join(configDir, "config.yaml")
	data, err := yaml.Marshal(config)
	if err != nil {
		return fmt.Errorf("failed to marshal config: %w", err)
	}

	if err := os.WriteFile(configFile, data, 0644); err != nil {
		return fmt.Errorf("failed to write config file: %w", err)
	}

	h.logger.Info("Created test context config",
		zap.String("context", h.ContextName),
		zap.String("config", configFile))

	// Now set the contract addresses using the context set command
	if err := h.setContractAddresses(); err != nil {
		return fmt.Errorf("failed to set contract addresses: %w", err)
	}

	return nil
}

// setContractAddresses uses the context set command to configure contract addresses
